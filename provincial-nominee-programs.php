      <h4 class="mt-6 fs-4 bg-gradient-dark text-white px-4 border-radius-lg shadow" style="text-shadow: 2px 2px 2px black; font-weight: normal;">How to apply for the Provincial Nominee Program ?</h4>

      <p>
        Following are the simple steps to get Canada provincial nomination program:
      </p>
      <p>
        <b>Step 1:</b> Check your eligibility for a PNP - Check through the above-mentioned list of PNPs under which PNP you are eligible to apply.
      </p>
      <p>
        <b>Step 2:</b> Submit your application - After selecting the PNP, you have to submit your application for provincial nomination.
      </p>
      <p>
        <b>Step 3:</b> Get your provincial nomination certificate - if your application is successfully evaluated by the provincial authority, you will be nominated for permanent residency in the province.
      </p>
      <p>
        <b>Step 4:</b> Submit your PR Visa application - With your provincial nomination, you have to submit your Canada PR Visa application to IRCC. If your PNP is in-line with the express entry program, you can apply online, if not you have to apply through a paper-based process.
      </p>


      <h4 class="mt-6 fs-4 bg-gradient-dark text-white px-4 border-radius-lg shadow" style="text-shadow: 2px 2px 2px black; font-weight: normal;">Applying for Canada PNP through a Paper-based process</h4>

      <p>
        If your PNP is not online with the express entry program, you have to apply through a paper-based process. 
      </p>
      <p>
        Once you get a provincial nomination, you can submit your paper application for Canadian permanent residence to IRCC with a PCC and a medical examination certificate.
      </p>
      <p>
        Paper-based processes take much longer than online-based processes through express entry programs. While the average processing time for a permanent residence application submitted through Express Entry is 6 months, a paper application is processed in 18 months on average.
      </p>

    
      <h4 class="mt-6 fs-4 bg-gradient-dark text-white px-4 border-radius-lg shadow" style="text-shadow: 2px 2px 2px black; font-weight: normal;">Applying for Canada PNP through the Express Entry process</h4>

      <p>
        Create an online account and submit an Express Entry profile. In your profile, you must indicate that you have been nominated by a Canadian province or territory.
        Once your provincial nomination is confirmed, you will be placed in the Express Entry pool and you will receive an additional 600 points for your CRS score. With these many extra points, you are effectively guaranteed to receive an ITA for Canada PR Visa.
      </p>

      
      <h4 class="mt-6 fs-4 bg-gradient-dark text-white px-4 border-radius-lg shadow" style="text-shadow: 2px 2px 2px black; font-weight: normal;">Express Entry Provincial Nominee Program</h4>

      <p>
        Most PNPs have an "enhanced" immigration stream that allows the provinces to search for skilled immigrants who can meet the needs of the labor market in the federal government's Express Entry pool. This way Express entry program's candidates can apply for provincial nomination. 
      </p>
      <p>
        After getting a provincial nomination certificate, Express entry candidates are awarded 600 CRS points to their profile, ensuring an ITA in the next rounds of invitation.
      </p>

    
      <h4 class="mt-6 fs-4 bg-gradient-dark text-white px-4 border-radius-lg shadow" style="text-shadow: 2px 2px 2px black; font-weight: normal;">What is the processing time for a PNP ?</h4>

      <p>
        Application processing time for a PNP depends on whether you have applied through express entry linked streams or non-express entry linked streams. 
      </p>
      <p>
        Total Canada PNP processing time includes the initial application to the province, plus the additional processing time for the PR Visa application by the IRCC.
      </p>
      <p>
        Most provinces have reduced their processing times to months or even weeks. For Express Entry linked streams, the processing time is 6 to 8 months. Non-Express Entry applications (paper-based)typically take between 1 and 2 years to be processed at the federal stage.
      </p>



