          <i><img src="picture/icon/b4.png"></i>
          <a class="w-100" data-bs-toggle="collapse" href="#q28" role="button" aria-expanded="false" aria-controls="q28">
            28. What’s the difference between investing through an EB-5 Regional Center and investing directly on my own?
          </a>
        </p>
        <div class="collapse" id="q28">
          <div class="card card-body">
           <p>A regional center gets investment from multiple investors for funding a project. A foreign national investing directly is on their own, so to speak. The main differences between the two routes to EB-5 are -</p>  
           <ol>
            <li>
              <b>Involvement:</b> Regional Centers don’t require every individual investor to be directly involved. A direct investor must be directly involved with the day-to-day working of their US business to maintain their EB-5 status.
            </li>
            <li>
              <b>Number of investors:</b> Regional Centers have many investors coming together in a mutual investment. EB-5 private investment is limited to a single individual.
            </li>
            <li>
              <b>Job creation and proving of jobs:</b> Regional Centers can create direct and indirect jobs. The job creation can be proved on an estimate and don’t have to be proved directly. Private investments require proving the creation of 10 direct jobs. 
            </li>
            <li>
              <b>Control:</b> Regional Centers provide limited control. A direct investment generally makes you the sole owner.  
            </li>
            <li>
              <b>Ownership:</b> Regional Centers don’t offer ownership as the investment is by many. A direct investment tends to get you complete ownership. 
            </li>
           </ol> 
    
