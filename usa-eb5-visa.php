              Employ a minimum of 10 full-time workers
            </li>
            <li>
              Lead to a product or service production, and 
            </li>
            <li>
              Benefit the US economy.
            </li>
           </ol> 
           <p>Independent contractors can’t be included as employees.</p> 
          </div>
