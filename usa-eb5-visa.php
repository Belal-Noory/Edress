        </p>
        <div class="collapse" id="q26">
          <div class="card card-body">
           <p>You will have to maintain your investment and be actively involved with the company for the next two years from being granted your EB-5 status. Once you have your unconditional green card, you can choose to work for another company or stop working altogether.</p> 
