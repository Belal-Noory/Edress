      <h4 class="mt-6 fs-4 bg-gradient-info text-white px-4 border-radius-lg shadow font-weight-normal">How Can I Apply for the Skilled Independent Subclass 189 Visa?</h4>

      <h5 class="text-info mt-6">
       Stage 1: Verify if your occupation is on the SOL - Skilled Occupation List
     </h5>
      <p>
           The Subclass 189 Visa is available only for individuals who can fill a position on the SOL. 
      </p>

      <h5 class="text-info mt-6">
           Stage 2: Ensure you fulfill all the eligibility requirements
      </h5>

      <p>
           To apply for the Subclass 189 Visa, you must fulfill several requirements and the most essential of these are:  
      </p>

      <p><i class="fa-solid fa-square-check text-success"></i>  
           Be below 45 years of age.    
      </p>
      <p><i class="fa-solid fa-square-check text-success"></i>  
           Have minimum skills in English.     
      </p>
      <p><i class="fa-solid fa-square-check text-success"></i>  
           Fulfill the character and health requirements.    
      </p>
      <p><i class="fa-solid fa-square-check text-success"></i>  
           Agree to sign the Statement of Australian Values.    
      </p>

      <h5 class="text-info mt-6">
           Stage 3: Complete an Assessment for Skills
      </h5>

      <p>
           You must complete an assessment for skills and score the required points to qualify for the Subclass 189 Visa. The score must be as per the specification on your letter of invitation (stage 5).   
      </p>

      <h5 class="text-info mt-6">
           Stage 4: Submit an EOI - Expression of Interest through Australia Skill Select
      </h5>

      <p>
           In the next stage, you are required to submit an EOI through Skill Select. The Australian Skill Select program will have several queries regarding your skills, background, and applicable employment history. Your EOI will be visible to authorities in Australia and to employers as well who might be seeking candidates for hiring having skill-sets similar to that of yours. You will be invited to apply for Subclass 189 Visa if your EOI fulfills the Visa requirements and you obtain a sponsor or nomination.   
      </p>

      <h5 class="text-info mt-6">
           Stage 5: Obtain a government nomination or identify a sponsor
      </h5>

      <p>
           The Skilled Independent Subclass 189 Visa requires you to receive a nomination from a Territory or State Government or Sponsorship from a family member or employer in Australia. After you obtain the appropriate sponsor, you will receive the ITA for the Visa from them.    
      </p>
      <p>
           You can find an employer through the Skill Select program if you do not have a qualified relative or employer for the sponsorship. This program connects qualified skilled workers with employers seeking skilled overseas workers. You can also receive the nomination from a Territory or State Government through Skill Select.     
      </p>

      <h5 class="text-info mt-6">
           Stage 6: Get your sponsor to complete essential paperwork
      </h5>

      <p>
           After you get a sponsor, you must ask them to nominate you for the Subclass 189 Visa. Often, you can apply for this Visa at the same time your sponsor files their nomination application. Employers can submit the nomination application online through DHA.     
      </p>

      <h5 class="text-info mt-6">
           Stage 7: Apply for the Skilled Independent Subclass 189 Visa
      </p>

      <p>
           Once you identify an employer and receive the nomination, you can apply online for the Subclass 189 Visa with the DHA.     
      </p>

      <h5 class="text-info mt-6">
           Stage 8: Await for the verdict
      </h5>

      <p>
           The DHA can take several months to declare its verdict on your Subclass 189 Visa application.      
      </p>

      <h5 class="text-info mt-6">
           Stage 9: Obtain your visa and begin working in Australia
      </h5>

      <p>
           You will receive your Skilled Independent Subclass 189 Visa once the DHA approves your application. You can then begin to work in Australia.       
      </p>





