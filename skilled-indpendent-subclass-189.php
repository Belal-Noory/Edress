         To apply for Australia Skilled Independent Subclass 189 Visa you must score at least 65 points on the Australia 65 Points Calculator. You must also submit an EOI - Expression of Interest and receive an ITA - Invitation to Apply from Skill Select. Once you receive the ITA, you get 60 days to submit your Visa application. You are required to submit all the necessary documents to support claims made in your profile while submitting the EOI. 
      </p>
